public class switch_statements {
    
    public static void main(String[] args){
        
        int age = 16;
        
        switch(age){
            
            case 15 :
                System.out.println("Your age is 15 ");
                break;
                
            case 16 :
                System.out.println("Your age is 16 ");
                break;
                
            case 17 :
                System.out.println("Your age is 17 ");
                break;
                
            default :
                System.out.println("I don't no");
                break;
                
                
                
                        
        }        
   
    }
    
}
